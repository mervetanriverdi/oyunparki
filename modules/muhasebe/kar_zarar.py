def hesapla_kar_giderler(gelir, gider):
    """
    Gelir ve gider farkını hesaplar (kâr veya zarar).
    
    :param gelir: Toplam gelir (int veya float)
    :param gider: Toplam gider (int veya float)
    :return: Net kâr (pozitif) veya zarar (negatif)
    """
    return gelir - gider
